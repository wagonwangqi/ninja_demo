DROP TABLE IF EXISTS staff,member,mem_cou,sta_cou,course;
create table staff
(
    sta_id   BIGSERIAL              NOT NULL primary key,
    sta_name character varying(255) NOT NULL UNIQUE
);
create table member
(
    mem_id   bigserial              not null primary key,
    mem_name character varying(255),
    login_id CHARACTER VARYING(255) NOT NULL UNIQUE,
    level    character varying(255) not null,
    email    CHARACTER VARYING(255),
    uid      CHARACTER VARYING(255) NOT NULL UNIQUE,
    password CHARACTER VARYING(255) NOT NULL,
    homework_id bigint references homework (homework_id)
);
create table course
(
    cou_id   bigserial not null primary key,
    cou_name character varying(255),
    cou_pic  character varying(255),
    cou_vid  character varying(255),
    cou_des  character varying(255),
    smallCategory_name character varying(255),
    bigCategory_name character varying(255),
    sta_id bigint,
    created    TIMESTAMP WITHOUT TIME ZONE,
    modified   TIMESTAMP WITHOUT TIME ZONE
);

create table mem_cou
(
    id         BIGINT not null generated by default as identity,
    mem_id     bigint not null references member (mem_id),
    cou_id     bigint not null references course (cou_id),
    is_collect boolean,
    primary key (id)
);
create table sta_cou
(
    sta_id bigserial not null references staff (sta_id),
    cou_id bigserial not null references course (cou_id)
)
